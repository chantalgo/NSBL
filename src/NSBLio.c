#include <string.h>
#include <stdlib.h>
#include "NSBLio.h"

//function to print string
void printString(char* str)
{
	printf("%s",str);
}

//function to print float
void printFloat(float f)
{
	printf("%f",f);
}

//function to print integer
void printInteger(int i)
{
	printf("%d",i);
}


//function to break line(newline)
void printFromNewLine()
{
	printf("\n");
}

